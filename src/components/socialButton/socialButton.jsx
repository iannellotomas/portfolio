export default function SocialButton({ iconName, type = "primary" }) {
	const social = icons.find((item) => item.title === iconName);
	const [isSnackbarVisible, setIsSnackbarVisible] = useState(false);

	const handleEmail = () => {
		navigator.clipboard.writeText("iannello.mas@gmail.com");
		setIsSnackbarVisible(true);
	};

	const handleCloseSnackbar = () => {
		setIsSnackbarVisible(false);
	};

	return (
		<Tooltip
			text={social.label}
			size="minimal"
			anchorSide={type == "secondary" ? "top" : "center"}>
			<a
				className={`${styles.link} ${
					type == "secondary" ? styles.secondary : ""
				}`}
				href={social.url}
				rel="noreferrer"
				target={social.title != "email" ? "_blank" : null}
				onClick={social.title == "email" ? () => handleEmail() : null}>
				{social.svg}
			</a>
			{social.title == "email" && (
				<Snackbar
					title="Email copiado"
					iconName="link"
					isVisible={isSnackbarVisible}
					onClose={handleCloseSnackbar}
				/>
			)}
		</Tooltip>
	);
}
